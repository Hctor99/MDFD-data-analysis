def compute_correlation_matrix(traits_df, correction_method=None):
    # Compute correlation matrix
    traits_corrs = traits_df.corr()

    n = traits_df.shape[1]
    corr_matrix = pd.DataFrame(np.eye(n), columns=traits_df.columns, index=traits_df.columns)
    p_matrix = pd.DataFrame(np.ones((n, n)), columns=traits_df.columns, index=traits_df.columns)

    # Compute upper triangle of correlation and p-value matrices
    for i, col1 in enumerate(traits_df.columns):
        for j, col2 in enumerate(traits_df.columns):
            if i < j:
                traits_df[col1] = pd.to_numeric(traits_df[col1], errors='coerce')
                traits_df[col2] = pd.to_numeric(traits_df[col2], errors='coerce')
                r, p = pearsonr(traits_df[col1], traits_df[col2])
                corr_matrix.iloc[i, j] = r
                corr_matrix.iloc[j, i] = r
                p_matrix.iloc[i, j] = p
                p_matrix.iloc[j, i] = p
    if correction_method is not None:
        # Flatten the upper triangle (excluding diagonal) for correction
        mask = np.triu(np.ones_like(p_matrix, dtype=bool), k=1)
        pvals_flat = p_matrix.where(mask).stack()

        # Holm-Bonferroni correction
        rej, pvals_corrected, _, _ = multipletests(pvals_flat.values, alpha=0.05, method=correction_method)

        # Reconstruct corrected p-value matrix
        corrected_p_matrix = pd.DataFrame(np.ones((n, n)), columns=traits_df.columns, index=traits_df.columns)
        corrected_p_matrix.values[mask] = pvals_corrected
        corrected_p_matrix = corrected_p_matrix.T.combine_first(corrected_p_matrix)  # Make symmetric
        np.fill_diagonal(corrected_p_matrix.values, np.nan)  # Diagonal = NaN
        p_matrix = corrected_p_matrix

    mask = np.triu(np.ones_like(traits_corrs, dtype=bool))
    masked_corrs = traits_corrs.mask(mask)
    p_matrix = p_matrix.mask(mask)
    #Plot the correlation matrix
    plt.figure(figsize=(12,12))
    ax = plt.gca()
    im = ax.imshow(masked_corrs, cmap='coolwarm', interpolation='none')
    #Only plot the upper triangle

    #Print number of significant correlations (ie < 0.05)
    significant_corrs = p_matrix[p_matrix < 0.05].count().sum()
    pprint(f'Number of significant correlations (p < 0.05): {significant_corrs}')
    column_names = [col.capitalize().replace('_', ' ') for col in traits_corrs.columns]
    plt.xticks(range(len(column_names)), column_names, rotation=45, ha='right')
    plt.yticks(range(len(column_names)), column_names)

    # Loop through correlation matrix to display correlation values and significance
    for i in range(len(traits_corrs.columns)):
        for j in range(len(traits_corrs.columns)):
          #  corr_value = traits_corrs.iloc[i, j]
            p_value = p_matrix.iloc[i, j]

            # Determine significance level
            if p_value < 0.001:
                stars = '***'
            elif p_value < 0.01:
                stars = '**'
            elif p_value < 0.05:
                stars = '*'
            else:
                stars = ''
            
            # Display the correlation value with significance stars
            plt.text(j, i, stars, ha='center', va='center', color="black")
    
    divider = make_axes_locatable(ax)
    cax = divider.append_axes("right", size="4%", pad=0.4)
    ax.grid(None)
    correction_label = 'uncorrected' if correction_method==None else 'corrected'
    fig_name = f'figures/actor-traits_correls_{correction_label}.png'
    plt.colorbar(im, cax=cax)
    plt.savefig(fig_name, dpi=150, bbox_inches='tight')
    plt.close()
    print('Saving figure:', fig_name)

