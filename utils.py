def plot_mini_grid(grid_val_df, grid_aro_df, col, coloring, cmap='viridis', colorbar_label=None, 
                   title=None, save_figure=True, var='', normalized_grid=''):
    
    if 'G' in col:                      #Subject selection
        grid_df = np.array([grid_val_df[col], grid_aro_df[col]]).T
    elif 'F' in col or 'M' in col:      #Actor selection
        grid_df = np.array([grid_val_df[col], grid_aro_df[col]]).T
    elif col == 'all' or col is None:   #All data selection
        grid_df = np.array([grid_val_df, grid_aro_df]).T
    else:
        raise ValueError(f"Invalid column selection: {col}. Must be actor, subject, or 'all' / None.")
    grid_df = grid_df[~np.isnan(grid_df).any(axis=1)].T
    coloring = coloring[~np.isnan(grid_df).any(axis=0)]

    if colorbar_label is not None:
        plt.figure(figsize=(9, 7), dpi=80)
    else:
        plt.figure(figsize=(7, 7), dpi=80)
    dps_size = 120 if len(coloring) < 100 else 20
    if cmap == 'categorical':
        cmap = sns.color_palette("Set1", n_colors=len(np.unique(coloring)))
        coloring = [cmap[int(i)] for i in coloring]
        plt.scatter(grid_df[0,:], grid_df[1,:], c=coloring, s=dps_size, zorder=1)
    else:
        plt.scatter(grid_df[0,:], grid_df[1,:], c=coloring, cmap=cmap, s=dps_size, zorder=1)
    plt.axis('equal')
    plt.xticks([])
    plt.yticks([])
    if normalized_grid:
        plt.ylim([-5,5])
        plt.xlim([-5,5])
        plt.axhline(linewidth=3, color='black', zorder=0)
    else:
        plt.ylim([0, 300])
        plt.xlim([-150, 150])
        plt.axhline(150, linewidth=3, color='black', zorder=0)
    plt.axvline(linewidth=3, color='black', zorder=1)
