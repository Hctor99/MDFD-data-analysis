def compute_jsd(density_array, base=2):
    """
    Computes the average pairwise Jensen-Shannon divergence between a set of 2D densities.
    (Generated by ChatGPT)
    """
    n = density_array.shape[0]
    flat_densities = []

    # Flatten and normalize each distribution
    for i in range(n):
        p = density_array[i].ravel()
        p = np.clip(p, 1e-10, None)  # avoid log(0)
        p /= p.sum()  # normalize to sum to 1
        flat_densities.append(p)
    
    # Compute all pairwise JSDs
    jsd_vals = []
    for i in range(n):
        for j in range(i + 1, n):
            jsd = jensenshannon(flat_densities[i], flat_densities[j], base=base)**2
            jsd_vals.append(jsd)
    
    return np.mean(jsd_vals)

