def labels_mean_scores(df):
    ''' Calculates mean and std of 4-AFC responses in Experiment 1. Moreover, conducts binomial tests to assess recognition accuracy. 
    '''
    participants = [col for col in df.columns if 'L' in col]
    conditions = df['condition'].unique()
    actors = df['actor'].unique()

    #Calculate means per condition
    condition_means_df = {}
    for condition in conditions:
        condition_df = df[df['condition'] == condition]
        xs = condition_df[participants].values.reshape(-1,)
        #Since we're missing one stimuli in two actors...
        k = np.nansum(xs)
        n = xs.shape[0] if not np.isnan(list(xs)).any() else xs.shape[0]-1
        #print(xs.shape[0])
        p_val = round(binomtest(k, n, p=0.25, alternative='greater').pvalue, 4)
        mean = np.nanmean(xs)
        #In order not to artificially inflating sample size, divide by num of participants NOT by num of responses (n)
        std = np.array(np.sqrt(mean * (1 - mean) / len(participants)))
        condition_means_df[condition] = [mean, std, p_val]
    condition_means_df = pd.DataFrame(condition_means_df).T
    condition_means_df.columns = ['MEAN', 'STD', 'pval']

    #Calculate means per actor
    actor_means_df = {}
    for actor in actors:
        actor_df = df[df['actor'] == actor]
        xs = actor_df[participants].values.reshape(-1,)
        #Since we're missing one stimuli in two actors...
        k = np.nansum(xs)
        n = xs.shape[0] if not np.isnan(list(xs)).any() else xs.shape[0]-1
     #   print(n)
        p_val = round(binomtest(k, n, p=0.25, alternative='greater').pvalue, 4)
        mean = np.nanmean(xs)
        #In order not to artificially inflating sample size, divide by num of participants NOT by num of responses (n)
        std = np.array(np.sqrt(mean * (1 - mean) / len(participants)))
        actor_means_df[actor] = [mean, std, p_val]
    actor_means_df = pd.DataFrame(actor_means_df).T
    actor_means_df.columns = ['MEAN', 'STD', 'pval']
    return condition_means_df, actor_means_df

