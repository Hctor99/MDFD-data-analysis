def compute_isomap(data, n_components=1, n_neighbors=10):
    isomap = Isomap(n_components=n_components, n_neighbors=n_neighbors)
    embedding = isomap.fit_transform(data)
    eigenvalues = isomap.kernel_pca_.eigenvalues_
    explained_variance_ratio = eigenvalues / np.sum(eigenvalues)
    explained_variance_ratio = np.round(explained_variance_ratio, 3)

    #Get geodesic distances from the neutral point (closest to 0 in first component)
    pc1 = embedding[:, 0]
    neutral_index = np.argmin(np.abs(pc1))
    dist_from_neutral = isomap.dist_matrix_[neutral_index]

    return embedding, explained_variance_ratio, dist_from_neutral

def get_best_n_neighbors(grid_actor_df, n_components=5):
    best_NN = 0
    best_var = 0
    for n_NN in range(3,11):
        _, components_explained_variance, _ = compute_isomap(grid_actor_df, n_components=n_components, n_neighbors=n_NN)
        if components_explained_variance[0] > best_var:
            best_var = components_explained_variance[0]
            best_NN = n_NN
    return best_NN

